// Notes:
// How  to distinguish id, class and html elements
// HTML Elements: directly by their tag name. 
// For example:
// p { }
// div { }

// Classes:  by using a period (.)
// For example:
// .my-class { }
// .other-class { }

// IDs: by using a hash (#) followed by the ID name
// For example:
// #my-id { }
// #other-id { }